    private void viewDetailsjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsjButtonActionPerformed
        // TODO add your handling code here:
        int selectedrow = VitalSignsjTable.getSelectedRow();
        if(selectedrow>=0)
        {
            VitalSigns vs = (VitalSigns)VitalSignsjTable.getValueAt(selectedrow, 0);
            temperatureTextField.setText(String.valueOf(vs.getTemperature()));
            bloodPressureTextField.setText(String.valueOf(vs.getBloodPressure()));
            pulseTextField.setText(String.valueOf(vs.getPulse()));
            dateTextField.setText(vs.getDate());
            
        setAllEnabled(false);


        }
        else
        JOptionPane.showMessageDialog(null,"Select any row");
    }//GEN-LAST:event_viewDetailsjButtonActionPerformed

    private void deletejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletejButtonActionPerformed
        // TODO add your handling code here:
        int selectedrow = VitalSignsjTable.getSelectedRow();
        if(selectedrow>=0)
        {
            VitalSigns vs = (VitalSigns)VitalSignsjTable.getValueAt(selectedrow, 0);
            vsh.deleteVitals(vs);
            JOptionPane.showMessageDialog(null,"Vital Sign has been deleted");
            populateTable();

        }
        else
        JOptionPane.showMessageDialog(null,"Select any row");
    }//GEN-LAST:event_deletejButtonActionPerformed

    private void updatejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatejButtonActionPerformed
        int selectedrow = VitalSignsjTable.getSelectedRow();
        if(selectedrow>=0)
        {

        setAllEnabled(true);


            VitalSigns vs = (VitalSigns)VitalSignsjTable.getValueAt(selectedrow, 0);

            temperatureTextField.setText(String.valueOf(vs.getTemperature()));
            bloodPressureTextField.setText(String.valueOf(vs.getBloodPressure()));
            pulseTextField.setText(String.valueOf(vs.getPulse()));
            dateTextField.setText(vs.getDate());

            populateTable();

        }else{
            JOptionPane.showMessageDialog(null, "Select a row");
        }
    }//GEN-LAST:event_updatejButtonActionPerformed

    private void confirmjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmjButtonActionPerformed
        // TODO add your handling code here:
        int selectedrow = VitalSignsjTable.getSelectedRow();
                if(selectedrow>=0)
        {
            
        setAllEnabled(true);

            
          VitalSigns vs = (VitalSigns)VitalSignsjTable.getValueAt(selectedrow, 0);
          vs.setTemperature(Double.parseDouble(temperatureTextField.getText()));
          vs.setBloodPressure(Double.parseDouble(bloodPressureTextField.getText()));
          vs.setPulse(Integer.parseInt(pulseTextField.getText()));
          vs.setDate(dateTextField.getText());
          
          populateTable();
        setAllEnabled(false);

          
        
        }else{
                    JOptionPane.showMessageDialog(null, "Select a row");
                }
                                  
    }//GEN-LAST:event_confirmjButtonActionPerformed


