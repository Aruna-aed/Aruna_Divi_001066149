    public void populateTable(){
    DefaultTableModel dtm = (DefaultTableModel)VitalSignsjTable.getModel();
    dtm.setRowCount(0);
    for(VitalSigns vs : vsh.getVitalSignHistory()){
        Object row[]= new Object[2];
        row[0]=vs;
        row[1]=vs.getBloodPressure();
        if(Double.parseDouble(row[1].toString())< this.MIN_BP || Double.parseDouble(row[1].toString()) > this.MAX_BP)
        dtm.addRow(row);
        
    }
}

